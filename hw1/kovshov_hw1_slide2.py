"""4. Вычислить сумму и произведение всех натуральных чисел от А до В включительно."""
print("Начало четвёртого задания")


def func_4(a, b):
    sum = 0
    multi = 1
    for i in range(a, b + 1):
        sum += i
        multi *= i
    print("Sum: " + str(sum) + " Multi: " + str(multi))


print("Сумма и произведение чисел от 3 до 6")
func_4(3, 6)

print("Сумма и произведение чисел от 7 до 9")
func_4(7, 9)

print("Конец четвёртого задания")

"""5. Даны натуральные числа А и В. Вывести сначала все чётные числа, заключённые
между ними, потом все нечётные (генератором/ами списков)"""
print("Начало пятого задания")


def func_5(a, b):
    even = []
    odd = []
    for i in range(a, b + 1):
        if i % 2 == 0:
            even.append(i)
        else:
            odd.append(i)
    print("Чётыне: " + str(even))
    print("Не чётыне: " + str(odd))


print("Чёт и не чёт от 3 до 6")
func_5(3, 6)

print("Чёт и не чёт от 4 до 10")
func_5(4, 10)

print("Конец пятого задания")

"""6. Исходный список содержит положительные и отрицательные числа. Требуется
положительные поместить в один список, а отрицательные - в другой
(генератором/ами списков)"""
print("Начало шестого задания")


def func_5(list_a):
    more_than_zero = []
    less_than_zero = []
    for i in range(len(list_a)):
        if list_a[i] < 0:
            less_than_zero.append(list_a[i])
        else:
            more_than_zero.append(list_a[i])
    print("Больше нуля: " + str(more_than_zero))
    print("Меньше нуля: " + str(less_than_zero))


func_5([-1, -7, 3, 6, -4, 4, 5, 1 - 3])

print("Конец шестого задания")
