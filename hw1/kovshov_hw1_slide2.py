"""1. Найти N-й член последовательности 1, 1, 2, 3, 5, 8, 13..."""
print("Начало первого задания")


def func_1(n):
    a1 = 1
    a2 = 1
    for i in range(n - 2):
        tmp = a1 + a2
        a1 = a2
        a2 = tmp
    return str(a2)


print("3 член последовательности " + func_1(3))
print("4 член последовательности " + func_1(4))
print("5 член последовательности " + func_1(5))
print("6 член последовательности " + func_1(6))
print("7 член последовательности " + func_1(7))
print("Конец первого задания")

